    public function test_establishment_creates_branch_and_emission_point_idempotently(): void
    {
        $second = $this->createSecondTenant();
        \Laravel\Sanctum\Sanctum::actingAs($second['user']);

        // Create company first.
        $this->postJson('/api/v1/onboarding/company', [
            'ruc' => '1790012345001',
            'business_name' => 'Empresa Demo',
            'address' => 'Direccion',
            'email' => 'demo@example.com',
            'taxpayer_type' => 'natural',
            'sri_environment' => '1',
        ])->assertCreated();

        $first = $this->postJson('/api/v1/onboarding/establishment', [
            'address' => 'Matriz address',
        ]);
        $first->assertOk()
            ->assertJsonPath('data.branch.code', '001')
            ->assertJsonPath('data.emission_point.code', '001');

        $branchId = $first->json('data.branch.id');
        $epId = $first->json('data.emission_point.id');

        // Re-submitting with same codes must update, not duplicate.
        $second2 = $this->postJson('/api/v1/onboarding/establishment', [
            'address' => 'Updated address',
        ]);
        $second2->assertOk()
            ->assertJsonPath('data.branch.id', $branchId)
            ->assertJsonPath('data.emission_point.id', $epId)
            ->assertJsonPath('data.branch.address', 'Updated address');

        $this->assertSame(1, \App\Models\Tenant\Branch::where('tenant_id', $second['tenant']->id)->count());
        $this->assertSame(1, EmissionPoint::where('tenant_id', $second['tenant']->id)->count());
    }

    public function test_establishment_imports_open_sri_establishments_when_requested(): void
    {
        \Illuminate\Support\Facades\Http::fake([
            '*Establecimiento*' => \Illuminate\Support\Facades\Http::response([
                [
                    'nombreFantasiaComercial' => null,
                    'direccionCompleta' => 'GUAYAS / GUAYAQUIL / XIMENA',
                    'estado' => 'ABIERTO',
                    'numeroEstablecimiento' => '002',
                    'matriz' => 'SI',
                ],
                [
                    'nombreFantasiaComercial' => 'SUCURSAL NORTE',
                    'direccionCompleta' => 'PICHINCHA / QUITO / NORTE',
                    'estado' => 'ABIERTO',
                    'numeroEstablecimiento' => '003',
                    'matriz' => 'NO',
                ],
                [
                    'nombreFantasiaComercial' => 'CERRADA',
                    'direccionCompleta' => 'LOS RIOS / VINCES',
                    'estado' => 'CERRADO',
                    'numeroEstablecimiento' => '001',
                    'matriz' => 'NO',
                ],
            ]),
        ]);

        $second = $this->createSecondTenant();
        \Laravel\Sanctum\Sanctum::actingAs($second['user']);

        $this->postJson('/api/v1/onboarding/company', [
            'ruc' => '1207481803001',
            'business_name' => 'Empresa Demo',
            'address' => 'Direccion',
            'email' => 'demo@example.com',
            'taxpayer_type' => 'natural',
            'sri_environment' => '1',
        ])->assertCreated();

        $response = $this->postJson('/api/v1/onboarding/establishment', [
            'code' => '002',
            'address' => 'GUAYAS / GUAYAQUIL / XIMENA',
            'import_sri_establishments' => true,
        ]);

        $response->assertOk()
            ->assertJsonPath('data.branch.code', '002')
            ->assertJsonPath('data.imported_branches.0.code', '003');

        // Matriz (002) + sucursal abierta importada (003); la cerrada (001) no
        $this->assertSame(2, \App\Models\Tenant\Branch::where('tenant_id', $second['tenant']->id)->count());
        $this->assertDatabaseHas('branches', [
            'tenant_id' => $second['tenant']->id,
            'code' => '003',
            'name' => 'SUCURSAL NORTE',
            'is_main' => false,
        ]);

        // La importada queda con punto de emisión operativo
        $imported = \App\Models\Tenant\Branch::where('tenant_id', $second['tenant']->id)
            ->where('code', '003')->first();
        $this->assertSame(1, $imported->emissionPoints()->count());
    }
}
